namespace Spryker\Zed\Store;

use Spryker\Shared\Kernel\Store;
use Spryker\Zed\Kernel\AbstractBundleDependencyProvider;
use Spryker\Zed\Kernel\Container;
use Spryker\Zed\Store\Dependency\StoreToKernelStoreBridge;

class StoreDependencyProvider extends AbstractBundleDependencyProvider
{

    const KERNEL_STORE = 'store';

    /**
     * @param \Spryker\Zed\Kernel\Container $container
     *
     * @return \Spryker\Zed\Kernel\Container
     */
    public function provideBusinessLayerDependencies(Container $container)
    {
        $container = $this->addStore($container);

        return $container;
    }

    /**
     * @param \Spryker\Zed\Kernel\Container $container
     *
     * @return \Spryker\Zed\Kernel\Container
     */
    protected function addStore(Container $container)
    {
        $container[static::KERNEL_STORE] = function () {
            return new StoreToKernelStoreBridge(Store::getInstance());
        };

        return $container;
    }

}
