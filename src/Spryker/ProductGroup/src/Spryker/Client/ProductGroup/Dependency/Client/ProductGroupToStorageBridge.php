namespace Spryker\Client\ProductGroup\Dependency\Client;

class ProductGroupToStorageBridge implements ProductGroupToStorageInterface
{

    /**
     * @var \Spryker\Client\Storage\StorageClientInterface
     */
    protected $storageClient;

    /**
     * @param \Spryker\Client\Storage\StorageClientInterface $localeClient
     */
    public function __construct($localeClient)
    {
        $this->storageClient = $localeClient;
    }

    /**
     * @param string $key
     *
     * @return mixed
     */
    public function get($key)
    {
        return $this->storageClient->get($key);
    }

}
